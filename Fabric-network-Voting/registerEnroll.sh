#!/bin/bash

function createcommissioner() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/commissioner.voting.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/commissioner.voting.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-commissioner --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-commissioner.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-commissioner.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-commissioner.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-commissioner.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy commissioner's CA cert to commissioner's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/tlscacerts/ca.crt"

  # Copy commissioner's CA cert to commissioner's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/commissioner.voting.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/tlsca/tlsca.commissioner.voting.com-cert.pem"

  # Copy commissioner's CA cert to commissioner's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/commissioner.voting.com/ca"
  cp "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/ca/ca.commissioner.voting.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-commissioner --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-commissioner --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-commissioner --id.name commissioneradmin --id.secret commissioneradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-commissioner -M "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-commissioner -M "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls" --enrollment.profile tls --csr.hosts peer0.commissioner.voting.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/commissioner.voting.com/peers/peer0.commissioner.voting.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-commissioner -M "${PWD}/organizations/peerOrganizations/commissioner.voting.com/users/User1@commissioner.voting.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/users/User1@commissioner.voting.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://commissioneradmin:commissioneradminpw@localhost:7054 --caname ca-commissioner -M "${PWD}/organizations/peerOrganizations/commissioner.voting.com/users/Admin@commissioner.voting.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/commissioner/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/commissioner.voting.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/commissioner.voting.com/users/Admin@commissioner.voting.com/msp/config.yaml"
}

